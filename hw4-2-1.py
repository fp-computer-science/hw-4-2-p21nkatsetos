grade = int(input("What is your numerical grade in this class? "))

if grade >= 93 < 100:
    print("Your grade is an A")
elif grade >= 90 < 92:
    print("Your grade is an A-")
elif grade >= 87 < 89:
    print("Your grade is a B+")
elif grade >= 83 < 86:
    print("Your grade is a B")
elif grade >= 80 < 82:
    print("Your grade is a B-")
elif grade >= 77 < 79:
    print("Your grade is a C+")
elif grade >= 73 < 76:
    print("Your grade is a C")
elif grade >= 70 < 72:
    print("Your grade is a C-")
elif grade >= 65 < 69:
    print("Your grade is a D+")
elif grade >= 60 < 64:
    print("Your grade is a D")
elif grade < 60:
    print("Your grade is an F")
